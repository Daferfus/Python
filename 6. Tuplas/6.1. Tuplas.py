#-----------------------------------------------------------------------------------------------------------
# Crear Tupla
#-----------------------------------------------------------------------------------------------------------
# Una tupla es una secuencia de valores inmutables.
tupla = (25, 12, 14, 29)
print(tupla)

# No es lo más habitual omitir paréntesis, pero es lo mismo
tupla = 25, 12, 14, 29
print(tupla)

# Crear tupla de un solo valor.
tupla = (43,)
print(tupla)
#-----------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------


#-----------------------------------------------------------------------------------------------------------
# Acceder a Valores en Tupla
#-----------------------------------------------------------------------------------------------------------
tupla = (25, 12, 14, 29)
valor = tupla[1] + tupla[2]
print(valor)

tupla = (25, 12, 14, 29)
print(tupla[0])
#-----------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------


#-----------------------------------------------------------------------------------------------------------
# Funciones de Tupla
#-----------------------------------------------------------------------------------------------------------
print(len(tupla))

print(tupla.count(12))

print(tupla.index(14))

