# Métodos de la Clase String
#-----------------------------------------------------------------------------------------------------------
x = "esto es un mensaje"

print("upper:", x.upper())
print("title:", x.title())
print("replace:", x.replace("es","kk"))
print("format:", "La edad de {0} es {1}".format("Pepe", 25))
print("split:", x.split(" "))
print("partition:", x.partition("un")) # devuelve una lista, esto lo veremos más adelante

