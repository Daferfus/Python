# También podemos pasar un número arbitrario de problemas.
def ordenar2(*args): 
  """
  Args: *args (list[])
  """
  lista = list(args) #creamos una lista con los valores recibidos
  lista.sort()
  print(lista)

#------------main-------------
ordenar2(5, 2, 7, 4)
ordenar2(7, 2, 8, 6, 3, 1, 5)
