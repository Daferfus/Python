# Función con Varios Valores
#-----------------------------------------------------------------------------------------------------------
def saludar2(edad, nombre = "Pepe"): 
  """
  Args: edad (int), nombre (default is "Pepe")
  Returns: string
  """
  return "Hola " + nombre + ", tienes " + str(edad) + " años"

#------------main-------------
res = saludar2(25)
print(res)
