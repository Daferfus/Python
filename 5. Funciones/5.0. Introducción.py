# Modificamos lista
def ordenar(lista): 
  """
  Args: lista (list[])
  Returns: list[]
  """
  lista.sort()
  lista[2] = 12 
  return lista

#------------main-------------
lista = [5, 2, 7, 4]
print("Después de llamar a la función: ", ordenar(lista), lista)
