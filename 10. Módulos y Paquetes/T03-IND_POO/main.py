def make_flights():
  """Crea vuelos y asigna pasajeros a ellos.

    Returns:
      f1 (Flight): Vuelos con pasajeros incluidos.
      f2 (Flight): Vuelos con pasajeros incluidos.
      f3 (Flight): Vuelos con pasajeros incluidos.
  """
  #-----------------------------------------------------------------------------------------------------------
  # Creaci칩n de Objetos
  #-----------------------------------------------------------------------------------------------------------
  f1 = Flight(number = "BA117", aircraft = Aircraft(registration = "G-EUAH", model = "Airbus A319", num_rows = 22, num_seats_per_row=6))
  f2 = Flight(number = "AF92", aircraft = Boeing(registration = "F-GSPS", airline = "Emirates"))
  f3 = Flight(number = "BA148", aircraft = Airbus(registration = "G-EUPT", variant = "A319-100"))
    
  p1 = Passenger(name = "Jack", surname = "Shephard", id_card = "85994003S")
  p2 = Passenger(name = "Kate", surname = "Austen", id_card = "12589756P")
  p3 = Passenger(name = "James", surname = "Ford", id_card = "56278665F")
  p4 = Passenger(name = "John", surname = "Locke", id_card = "10265448H")
  p5 = Passenger(name = "Sayid", surname = "Jarrah", id_card = "15758664M")
  #-----------------------------------------------------------------------------------------------------------
  #-----------------------------------------------------------------------------------------------------------
    
  #-----------------------------------------------------------------------------------------------------------
  # Llamadas de Funci칩n
  #-----------------------------------------------------------------------------------------------------------
  f1.allocate_passenger("12A", p1.passenger_data())
  f1.allocate_passenger("18F", p2.passenger_data())
  f1.allocate_passenger("18E", p3.passenger_data())
  f2.allocate_passenger("1C", p4.passenger_data())
  f2.allocate_passenger("4D", p5.passenger_data())

  f1.reallocate_passenger("12A", "5B")
  #-----------------------------------------------------------------------------------------------------------
  #-----------------------------------------------------------------------------------------------------------

  return f1, f2, f3
# ()
